# List example
friends = ["don", "xavier", "etienne", "donnie", "rhonda"]

print(friends)
print(friends[1])
print("\n")
#print element at index 2 and behind
print(friends[2:])
print("\n")
#print element at index 1 and before 3
print(friends[1:3])

#print last element in list
print(friends[-1])

friends[0] = "Barbara"
print(friends)
