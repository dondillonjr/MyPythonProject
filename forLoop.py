#forLoops
#for every letter do something
for letter in "test this":
    print(letter)

print("\n")

family = ["don", "rhonda", "donnie", "xavier", "etienne"]
for name in family:
    print(name)

print("\n");

for index in range(len(family)):
    print(family[index])

print("\n")
#prints all numbers between 0 and 5 not including 5
